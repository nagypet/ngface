package hu.perit.ngface.rest;

import hu.perit.ngface.core.types.intf.SubmitFormData;
import hu.perit.ngface.core.widget.form.Form;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestParam;

public interface NgfaceFormRestApi<T>
{
    @GetMapping
    Form getForm(@RequestParam(required = false) T id);

    @PostMapping
    void submitForm(@RequestBody SubmitFormData submitFormData);
}
